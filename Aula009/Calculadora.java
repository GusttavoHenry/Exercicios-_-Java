public class Calculadora{

   public int soma (int a, int b) {
       int resultado = a + b;
       return resultado;
}

    public int subtracao (int a, int b) {
       int resultado = a - b ;
       return resultado;
}

public int multiplicacao (int a, int b) {
    int resultado = a * b;
    return resultado;
}
public int divisao (int a, int b){
    int resultado = a / b;
    return resultado;
}
    
}